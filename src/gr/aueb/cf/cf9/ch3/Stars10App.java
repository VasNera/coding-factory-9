package gr.aueb.cf.cf9.ch3;

/**
 * Prints 10 horizontal stars
 */
public class Stars10App {
    public static void main(String[] args) {
        int i = 1;
        int j = 10;

        while (i <= 10) {
            System.out.println("*");
            i++;

        }
        while (j > 1) ;{
            System.out.println("*");
            j--;

        }
    }
}